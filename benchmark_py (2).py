# 4. Benchmark Function
def benchmark(model, x, runs=50):
    torch.cuda.synchronize()
    start = time.time()
    for _ in range(runs):
        with torch.no_grad():
            _ = model(x)
    torch.cuda.synchronize()
    end = time.time()
    return (end - start) / runs * 1000 # Convert to ms

